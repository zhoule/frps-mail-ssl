        domains+=("$frps_dashboard_domain")
    fi
    
    for domain in "${domains[@]}"; do
        log_info "ÈÖçÁΩÆÂüüÂêç: $domain"
        
        # ÁîüÊàênginxÈÖçÁΩÆ
        case "$domain" in
            "$frps_dashboard_domain")
