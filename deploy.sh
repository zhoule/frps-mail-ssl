                ;;
            "$frps_domain")
                generate_domain_ssl_config "$domain" "frps" "8880" "frps-api"
                ;;
            "$mail_domain")
                generate_domain_ssl_config "$domain" "stalwart-mail" "8080" "mail-web"
                ;;
